// TypeScript is a strongly typed programming language that builds on JavaScript, adding optional static typing. It is developed by Microsoft and is free and open-source. 

// Key Features:

// Static Typing:
// TypeScript allows developers to define types for variables, function parameters, and return values, enabling compile-time type checking. This helps catch errors early in the development process, reducing runtime bugs.

// Superset of JavaScript:
// TypeScript is a superset of JavaScript, meaning any valid JavaScript code is also valid TypeScript code. It adds additional syntax for type annotations and other features.

// Compilation to JavaScript:
// TypeScript code is compiled into JavaScript, which can be run in any JavaScript environment, including browsers, Node.js, and Deno.

// Enhanced Tooling:
// TypeScript provides rich tooling features, such as code completion, error checking, and refactoring, which improve developer productivity and code quality.

// Interfaces and Classes:
// TypeScript supports interfaces and classes, allowing developers to create well-structured and maintainable code.

// Type Inference:
// TypeScript can often infer types automatically, reducing the need for explicit type annotations.

// Null and Undefined Handling:
// TypeScript provides specific handling for null and undefined values, helping to prevent common errors related to these types.

